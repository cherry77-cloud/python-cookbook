"""
░░░░░░░░░░░░░░░░░░░░░░░░░░░░ 1.1 将序列分解为单独变量 (Sequence Unpacking) ░░░░░░░░░░░░░░░░░░░░░░░░░░░░
──────────────────────────────────────────────
◼️ 适用对象 : 所有可迭代对象 (元组 / 列表 / 字符串 / 文件对象 / 迭代器 / 生成器 …)
◼️ 数量匹配 : 变量数量必须与序列元素数量一致，否则抛 ValueError
◼️ 结构匹配 : 支持嵌套结构解包，例如 (year, mon, day) = date_tuple
◼️ 占位符   : 使用下划线 _ 忽略不需要的值
◼️ 部分解包 : Python 无专门语法，必须为每个元素提供变量名 (或占位符)
⚙️ 可迭代判定 : 对象只需实现 __iter__() 即视作 Iterable

万物皆对象 (identity / type / value)
────────────────────────────────────
• 身份 (identity) → id(obj)
• 类型 (type)     → type(obj)
• 值   (value)    → 对象内部保存的数据
"""

# 基本解包
x, y = (4, 5)

# 列表 + 嵌套元组
data = ['ACME', 50, 91.1, (2012, 12, 21)]
name, shares, price, (year, mon, day) = data  # 嵌套解包

# 使用占位符 _ 忽略不需要的值
_, shares, price, _ = data
